    private void btnModificarPersonaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarPersonaActionPerformed
        if (pc.getPersona().getNombre().equalsIgnoreCase(PersonaControlador.NCOMUN)) {
            cambiarPersona();
        } else {
            Integer i = JOptionPane.showConfirmDialog(this, "Esta seguro que quiere eliminar los datos?");
            if (i == JOptionPane.OK_OPTION) {
                cambiarPersona();
            }
        }
        Integer posCombo = cbxSelecionarPersona.getSelectedIndex();
        Utilidades.cargarComboPersonas(cbxSelecionarPersona, pc);
        cbxSelecionarPersona.setSelectedIndex(posCombo);
        cbxGeneroPersona.setSelectedIndex(0);
        cargarTabla();
    }//GEN-LAST:event_btnModificarPersonaActionPerformed

