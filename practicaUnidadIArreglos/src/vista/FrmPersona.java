    public void cambiarPersona() {
        Persona p = new Persona(txtNombrePersona.getText(), (TipoGenero) cbxGeneroPersona.getSelectedItem(),
                obtenerFechaNacimiento());
        cargarDatosPersonaCombo();
        pc.getPersona().setNombre(txtNombrePersona.getText());
        pc.getPersona().setGenero((TipoGenero) cbxGeneroPersona.getSelectedItem());
        pc.getPersona().setFechaNacimiento(obtenerFechaNacimiento());
        pc.getPersona().setFechaActual(new Date());
        pc.getPersona().setEdad(p.getEdad());
        pc.getPersona().setClasificacionEdad(p.getClasificacionEdad());
        
        Utilidades.cargarComboPersonas(cbxSelecionarPersona, pc);
        pc.setPersona(Utilidades.getPersonaCombo(cbxSelecionarPersona));
    }
    
    
    public void cargarTabla() {
        if (pc != null && pc.getPersona() != null) {
            mtp.setPersonas(pc.getPersonas());
            tblPersonas.setModel(mtp);
            tblPersonas.updateUI();
            jScrollPane1.setVisible(true);
        } else {
            jScrollPane1.setVisible(false);
        }

    }
    
    public void guardar() {
        if (txtNombrePersona.getText().isEmpty() || txtDia.getText().isEmpty() || txtMes.getText().isEmpty()
                || txtAnio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Datos incompletos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (pc != null && pc.getPersonas() != null) {
                Persona p = new Persona(txtNombrePersona.getText(), (TipoGenero) cbxGeneroPersona.getSelectedItem(),
                        obtenerFechaNacimiento());
                try {
                    pc.modificaPersona(p, fila);
                    JOptionPane.showMessageDialog(null, "Se ha modificado");
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Por favor genere Personas", "Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    
    public void limpiar() {
        txtNombrePersona.setText("");
        txtDia.setText("");
        txtMes.setText("");
        txtAnio.setText("");
        cargarTabla();
    }

    public Date obtenerFechaNacimiento() {
        Date dateFormat = new Date((Integer.parseInt(txtAnio.getText()) - 1900), (Integer.parseInt(txtMes.getText()) - 1), Integer.parseInt(txtDia.getText()));
        System.out.println(dateFormat);
        return dateFormat;
    }

