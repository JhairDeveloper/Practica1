    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (pc != null) {
            try {
                if (Utilidades.guardarArchivo(pc.getPersonas())) {
                    JOptionPane.showMessageDialog(null, "Se ha generado el archivo");
                } else {
                    JOptionPane.showMessageDialog(null, "No se genero el archivo", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Genere Personas", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Registre una persona", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


